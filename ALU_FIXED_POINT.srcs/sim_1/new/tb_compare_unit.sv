module tb_compare_unit;
    localparam int N = 32;

    logic signed [N-1:0] a, b;
    logic eq, lt, le;

    compare_unit #(.N(N)) dut (
        .a(a),
        .b(b),
        .eq(eq),
        .lt(lt),
        .le(le)
    );

    initial begin
        // ----------------------------------------------------
        // Equal case
        // ----------------------------------------------------
        a = 10; 
        b = 10; 
        #10;   // eq=1, lt=0, le=1

        // ----------------------------------------------------
        // a < b (both positive)
        // ----------------------------------------------------
        a = 5;  
        b = 10; 
        #10;   // eq=0, lt=1, le=1

        // ----------------------------------------------------
        // a > b (both positive)
        // ----------------------------------------------------
        a = 20;
        b = 7;
        #10;   // eq=0, lt=0, le=0

        // ----------------------------------------------------
        // Negative numbers
        // ----------------------------------------------------
        a = -3; 
        b = -5; 
        #10;   // eq=0, lt=0, le=0

        // ----------------------------------------------------
        // Negative vs positive
        // ----------------------------------------------------
        a = -1;
        b = 1;
        #10;   // eq=0, lt=1, le=1

        // ----------------------------------------------------
        // Boundary values
        // ----------------------------------------------------
        a = 32'sh7FFF_FFFF;   // INT_MAX
        b = 32'sh8000_0000;   // INT_MIN
        #10;   // a > b

        $finish;
    end

endmodule
