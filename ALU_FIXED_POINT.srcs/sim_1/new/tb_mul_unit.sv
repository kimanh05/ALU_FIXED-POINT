module tb_mul_unit;

    localparam int N = 32;
    localparam int F = 31;

    logic clk = 0;
    logic rst_n = 0;
    logic en = 1;

    logic signed [N-1:0] a, b;
    logic signed [N-1:0] y;

    // 100 MHz clock
    always #5 clk = ~clk;

    // DUT
    mul_unit #(
        .N(N),
        .F(F),
        .PIPE(2)
    ) dut (
        .clk  (clk),
        .rst_n(rst_n),
        .en   (en),
        .a    (a),
        .b    (b),
        .y    (y)
    );

    initial begin
        // ------------------------------------------------------------
        // Reset
        // ------------------------------------------------------------
        rst_n = 0;
        a = 0;
        b = 0;
        #20;
        rst_n = 1;

        // ------------------------------------------------------------
        // Test 1: 0.5 * 0.5 = 0.25
        // ------------------------------------------------------------
        // 0.5  = 0x40000000
        // 0.25 = 0x20000000
        a = 32'sh4000_0000;
        b = 32'sh4000_0000;
        #30;

        // ------------------------------------------------------------
        // Test 2: -1.0 * 0.25 = -0.25
        // ------------------------------------------------------------
        // -1.0 = 0x80000000
        // 0.25 = 0x20000000
        a = -32'sh8000_0000;
        b =  32'sh2000_0000;
        #30;

        // ------------------------------------------------------------
        // Test 3: -0.5 * -0.5 = +0.25
        // ------------------------------------------------------------
        a = -32'sh4000_0000;
        b = -32'sh4000_0000;
        #30;

        // ------------------------------------------------------------
        // Test 4: 0 * anything = 0
        // ------------------------------------------------------------
        a = 0;
        b = 32'sh7FFF_FFFF; // ~ +1.0
        #30;

        // ------------------------------------------------------------
        // Test 5: Rounding check (slightly above 0.5)
        // ------------------------------------------------------------
        // Expect rounding up
        a = 32'sh4000_0001;
        b = 32'sh4000_0000;
        #30;

        // ------------------------------------------------------------
        // Test 6: Pipeline stall (en = 0)
        // Output must hold previous value
        // ------------------------------------------------------------
        en = 0;
        a  = 32'sh4000_0000;
        b  = 32'sh4000_0000;
        #20;

        en = 1;
        #30;

        // ------------------------------------------------------------
        // End simulation
        // ------------------------------------------------------------
        $finish;
    end

endmodule
